# Desafio 005 Antecessor e Sucessor
# Faça um progama que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.
numero = int(input('Digite um número:'))
antecessor = numero - 1
sucessor = numero + 1
print('Analizando o número {} seu sucessor é {} e o seu antecessor é {}'.format(numero, sucessor, antecessor))
